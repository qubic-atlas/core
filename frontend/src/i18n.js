const STORAGE_KEY = "atlas-locale";

function initialLocale() {
  try {
    const saved = localStorage.getItem(STORAGE_KEY);
    if (saved && messages[saved]) return saved;
    const nav = (navigator.language || "en").slice(0, 2).toLowerCase();
    if (messages[nav]) return nav;
  } catch { /* SSR / privacy mode */ }
  return "en";
}

export const locale = ref(initialLocale());

export function setLocale(code) {
  if (!messages[code]) return;
  locale.value = code;
  try { localStorage.setItem(STORAGE_KEY, code); } catch { /* ignore */ }
  try { document.documentElement.setAttribute("lang", code); } catch { /* ignore */ }
}

function resolve(tree, key) {
  return key.split(".").reduce((o, k) => (o == null ? undefined : o[k]), tree);
}

// Look up a dot-path key in the active locale, falling back to English, then the key itself.
// Optional params interpolate {{name}} placeholders (double-brace, so literal {dest}/{tick} in
// code examples are left untouched).
export function t(key, params) {
  let s = resolve(messages[locale.value], key) ?? resolve(messages.en, key) ?? key;
  if (params && typeof s === "string")
    s = s.replace(/\{\{(\w+)\}\}/g, (_, k) => (params[k] ?? `{{${k}}}`));
  return s;
}
